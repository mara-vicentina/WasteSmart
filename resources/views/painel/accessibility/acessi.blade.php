            <p>
                3. Tecnologia Assistiva: Desenvolvimento e disponibilização de tecnologias que auxiliam pessoas com deficiência a acessar informações e interagir com o ambiente, como leitores de tela, teclados adaptados, sistemas de comunicação alternativa, entre outros.
            </p>
            <p>
                4. Comunicação Acessível: Garantia de que informações, documentos, websites e outros meios de comunicação sejam acessíveis para pessoas com deficiência visual, auditiva, cognitiva ou outras limitações, por meio de legendas, audiodescrição, linguagem de sinais, entre outros.
            </p>
            <p>
                5. Educação Inclusiva: Promoção de políticas educacionais que ofereçam oportunidades educacionais adequadas e inclusivas para estudantes com deficiência, adaptando métodos de ensino e infraestruturas físicas.
